    t.set(qn('xml:space'), 'preserve')
    t.text = text
    r.append(t)
    new_p.append(r)

    parent = insert_after.getparent()
    idx = list(parent).index(insert_after)
    parent.insert(idx + 1, new_p)
    state['after'] = new_p

