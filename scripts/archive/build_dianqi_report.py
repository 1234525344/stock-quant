def mk(run, fn, sz, bold=False):
    run.font.name = fn; run.font.size = sz; run.bold = bold
    rPr = run._r.find(qn('w:rPr'))
    if rPr is None: rPr = OxmlElement('w:rPr'); run._r.insert(0, rPr)
    rF = OxmlElement('w:rFonts')
    rF.set(qn('w:eastAsia'), fn); rF.set(qn('w:ascii'), fn); rF.set(qn('w:hAnsi'), fn)
    rPr.insert(0, rF)

def P(text='', fn=SONG, sz=Pt(12), bold=False, align=None, indent=0, sb=0, sa=0, ls=Pt(28)):
    p = D.add_paragraph()
    pf = p.paragraph_format; pf.line_spacing = ls; pf.line_spacing_rule = WD_LINE_SPACING.EXACTLY
    pf.space_before = Pt(sb); pf.space_after = Pt(sa)
    if indent: pf.first_line_indent = Pt(indent)
    if align == 'C': p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if align == 'R': p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    if text:
        r = p.add_run(text); mk(r, fn, sz, bold)
    return p

def blank(): return P('')
def Chap(text): return P(text, HEI, Pt(15), True, 0, 10, 6)
def Sec(text): return P(text, SONG, Pt(14), True, 0, 6, 3)
def Body(text): return P(text, SONG, Pt(12), False, 0, 24)
def Body0(text): return P(text, SONG, Pt(12), False, 0)

