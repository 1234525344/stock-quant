def make_table(headers, data, col_widths=None):
    """Create formatted table."""
    t = doc.add_table(rows=1 + len(data), cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = 'Table Grid'
    # Header
    for j, h in enumerate(headers):
        t.cell(0, j).text = h
        for p in t.cell(0, j).paragraphs:
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for r in p.runs: mk(r, HEI, Pt(10), True)
    # Data
    for i, row in enumerate(data):
        for j, val in enumerate(row):
            t.cell(i+1, j).text = str(val)
            for p in t.cell(i+1, j).paragraphs:
                p.alignment = WD_ALIGN_PARAGRAPH.CENTER
                for r in p.runs: mk(r, SONG, Pt(10))
    return t

