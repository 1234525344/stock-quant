Chap('2. 电力负荷计算方法与计算内容')
Sec('2.1 分支负荷电流的计算')
Body('住宅用电负荷与各分支线路负荷紧密相关。线路负荷的类型不同，其负荷电流的计算方法也不同。线路负荷一般分为纯电阻性负荷和感性负荷两类。')

Body('（1）纯电阻性负荷')
Body('纯电阻性负荷如白炽灯、电热器等，其电流可按下式计算：')
Body0('                    电流(A) = 功率(W) / 电压(V)')
Body('（2）感性负载')
Body('感性负荷如荧光灯、电视机、洗衣机等，其负荷电流可按下式计算：')
Body0('                    电流(A) = 功率(W) / (电压(V) × 功率因数cosφ)')
Body('当荧光灯没有电容补偿时其功率因数可取0.5～0.6，当荧光灯有电容补偿时其功率因数可取0.85～0.9。荧光灯的功率应为灯管功率与整流器功率之和。')
Body('（3）单相电动机')
Body('单相电动机如洗衣机、电冰箱用电动机的电流，可按下式计算：')
Body0('                    电流(A) = 功率(W) / (电压(V) × 功率因数 × 效率)')
Body('如果电动机铭牌上无功率因数和效率数据可查，则电动机的功率因数和效率都取0.75。')
Body('（4）用电总负荷电流不等于所有的用电设备电流之和')
Body('总负荷电流 = 用电量最大的一台家用电器的额定电流 + 同时用电效率 × 其余用电设备的额定电流之和。一般家庭同时用电率可取0.5～0.8。')

