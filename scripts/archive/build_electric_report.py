Chap('5. 用电安全设计')
Body('（1）电工规范中，要求埋暗管必须用PVC管和变头。')
Body('（2）注意强弱电线不能在同一管道内，会有干扰。')
Body('（3）电路改造有必要根据家电使用情况考虑进行线路增容。')
Body('（4）插座离地面一般为30cm，不应低于20cm，开关一般距地140cm。')
Body('（5）对空调、电热水器、微波炉等大容量电路设备，宜一台设备设置一个回路。如果合用一个回路，当它们同时使用时，导线易发热，即使不超过导线允许的工作温度，长期使用也会降低导线的绝缘性能。此外大容量用电回路的导线截面应适当加大，加大导线的截面可大大降低电能在导线上的损耗。')
Body('（6）在安装了漏电保护器后，为了加强安全保障，还可以安装保险丝。保险丝须用铅丝，如果采用铜丝，在夏季开启多个空调时，过大的电流可能会烧断保险丝。夏日潮湿多雷雨，如果发生电器设备被淋湿的情况，不要用手去触碰已受潮的设备。必须进行干燥处理，直到绝缘良好后，才能接通电源。')

Body('空气开关（微型断路器，MCB）：主要功能是在电线短路或用电量过负荷时自动"跳闸"，可以保护免于电线走火。如图5-1所示。')
Body('图5-1 空气开关示意图')

Body('漏电断路器：当检测到漏电时，会自动跳闸。一般漏电断路器要装在分路的回路上（如卫生间、厨房等的回路），而不是装在总开关处。因为跳闸时，若漏电断路器装在分路上，就只需在一个回路中找出漏电的地方；但若装在总开关上，则要对全部电路进行排查，非常麻烦，费用也更高。如图5-2所示。')
