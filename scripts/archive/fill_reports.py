def fill_report(template_path, output_path):
    """填写实验报告模板"""
    doc = Document(template_path)
    data_table_indices = [2, 4, 6, 8]
    filled_count = 0

    for exp_idx, table_idx in enumerate(data_table_indices):
        if exp_idx >= len(experiments):
            break
        if table_idx >= len(doc.tables):
            break

        table = doc.tables[table_idx]
        exp = experiments[exp_idx]

        # 填写"实验数据与结论"
        cell_data = table.cell(0, 1)
        for para in cell_data.paragraphs:
            for run in para.runs:
                run.text = ""
        cell_data.paragraphs[0].text = exp["data_conclusion"]
        for run in cell_data.paragraphs[0].runs:
            run.font.size = Pt(10)

        # 填写"实验心得与体会"
        cell_exp = table.cell(1, 1)
        for para in cell_exp.paragraphs:
            for run in para.runs:
                run.text = ""
        cell_exp.paragraphs[0].text = exp["experience"]
        for run in cell_exp.paragraphs[0].runs:
            run.font.size = Pt(10)

        filled_count += 1
        print(f"  ✅ 实验{exp_idx+1}: {exp['name']}")

    doc.save(output_path)
    print(f"\n✅ 报告已保存: {output_path}")
    print(f"   共填写 {filled_count} 份实验报告")


